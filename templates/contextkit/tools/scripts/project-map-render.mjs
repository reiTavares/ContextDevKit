/**
 * Project-map RENDER — turns the scanner model into the durable markdown docs.
 *
 * Pure functions (model in, strings out): no fs, no clock. The CLI writes what
 * these return. Three docs by design — the cheap INDEX the agent reads first,
 * then the per-module detail and the symbol inventory it opens only on demand.
 *
 * `00-index.md` is deliberately ~one screen: the whole economic point is that
 * the agent reads it instead of re-greping the tree. [project-map]
 */

const ROLE_EMOJI = { frontend: '🎨', backend: '⚙️', shared: '🔗', config: '🛠️', tests: '🧪' };

/**
 * Doc header. DELIBERATELY carries no date — the rendered docs are a pure
 * function of structure (only the `signature` varies, and only when the tree
 * changes), so regenerating an unchanged project produces a byte-identical file
 * and the committed map never churns. The generation timestamp lives in
 * `manifest.json` and the CLI console output. [project-map / ADR-0039]
 */
function header(model, title) {
  return [
    `# ${title} — ${model.name}`,
    '',
    `> Generated by \`/project-map\` · signature \`${model.signature}\`.`,
    "> Deterministic structural map (zero AI tokens). Re-run `/project-map` after big structural changes.",
    '',
  ];
}

/** Per-module display cap for the dependency adjacency list (keeps it scannable). */
const DEP_DISPLAY_CAP = 20;

/**
 * "Module dependencies" adjacency list (blast-radius edges). Sorted → diff-stable.
 * JS/TS-family edges only (ADR-0040); a note makes the absence honest. [project-map]
 */
function renderDependencies(model) {
  const withDeps = model.modules.filter((m) => (m.deps || []).length > 0);
  const out = ['', '## Module dependencies (who imports whom)', ''];
  if (withDeps.length === 0) {
    out.push('_No intra-repo edges detected (JS/TS imports). Other languages: deferred (ADR-0040)._');
    return out;
  }
  for (const m of withDeps) {
    const shown = m.deps.slice(0, DEP_DISPLAY_CAP).map((d) => `\`${d}/\``).join(', ');
    const more = m.deps.length > DEP_DISPLAY_CAP ? ` _(+${m.deps.length - DEP_DISPLAY_CAP} more)_` : '';
    out.push(`- \`${m.path}/\` → ${shown}${more}`);
  }
  return out;
}

/** 00-index.md — the one-screen overview the agent reads at the start of work. */
export function renderIndex(model) {
  const out = header(model, 'Project map');
  const s = model.stack;
  out.push('## Stack', '');
  out.push(`- **Languages:** ${s.languages.join(', ') || '—'}`);
  out.push(`- **Frameworks:** ${s.frameworks.join(', ') || '—'}`);
  out.push(`- **Package manager:** ${s.packageManager || '—'}${s.monorepo ? ' · **monorepo**' : ''}`);
  if (model.dataLayer.detected) out.push(`- **Data layer:** ${model.dataLayer.kinds.join(', ')}`);
  out.push('', `## Modules (${model.modules.length}) · ${model.fileCount} source files`, '');
  out.push('| Role | Module | Files | Languages |');
  out.push('| --- | --- | ---: | --- |');
  for (const m of model.modules) {
    out.push(`| ${ROLE_EMOJI[m.role] || ''} ${m.role} | \`${m.path}/\` | ${m.files}${m.capped ? '+' : ''} | ${m.languages.join(', ')} |`);
  }
  out.push(...renderDependencies(model));
  out.push('', '## How to use this map', '');
  out.push('- Read this index FIRST to orient — it replaces re-greping the tree.');
  out.push('- `01-modules.md` — per-module detail (role, languages, size).');
  out.push('- `02-inventory.md` — sampled exported symbols per module.');
  out.push('- Stale? Run `/project-map` to regenerate · `/project-map --check` to diff.');
  out.push('');
  return out.join('\n');
}

/** 01-modules.md — frontend/backend/shared breakdown with per-module facts. */
export function renderModules(model) {
  const out = header(model, 'Modules');
  const byRole = {};
  for (const m of model.modules) (byRole[m.role] ||= []).push(m);
  for (const role of ['frontend', 'backend', 'shared', 'config', 'tests']) {
    const mods = byRole[role];
    if (!mods?.length) continue;
    out.push(`## ${ROLE_EMOJI[role] || ''} ${role[0].toUpperCase()}${role.slice(1)}`, '');
    for (const m of mods) {
      out.push(`### \`${m.path}/\``);
      out.push(`- Languages: ${m.languages.join(', ') || '—'}`);
      out.push(`- Source files: ${m.files}${m.capped ? ' (capped — large module)' : ''}`);
      out.push(`- Symbols sampled: ${m.symbols.length}`);
      out.push('');
    }
  }
  return out.join('\n');
}

/** 02-inventory.md — the sampled symbol inventory per module (capped). */
export function renderInventory(model) {
  const out = header(model, 'Symbol inventory');
  out.push('> Sampled exported symbols (capped per module). Not exhaustive — a navigation aid.', '');
  for (const m of model.modules) {
    if (!m.symbols.length) continue;
    out.push(`## \`${m.path}/\` · ${ROLE_EMOJI[m.role] || ''} ${m.role}`, '');
    const byFile = {};
    for (const sym of m.symbols) (byFile[sym.file] ||= []).push(sym.name);
    for (const [file, names] of Object.entries(byFile)) {
      out.push(`- \`${file}\` — ${[...new Set(names)].join(', ')}`);
    }
    out.push('');
  }
  return out.join('\n');
}

/** All docs keyed by filename — the CLI writes each into the project-map dir. */
export function renderAll(model) {
  return {
    '00-index.md': renderIndex(model),
    '01-modules.md': renderModules(model),
    '02-inventory.md': renderInventory(model),
  };
}
