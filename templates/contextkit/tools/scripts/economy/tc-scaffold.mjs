/**
 * Task-Compiler: scaffold-from-pattern — materialise new files from a named
 * pattern template (WF0022 / ADR-0089).
 *
 * Single responsibility: locate a registered pattern, render its templates,
 * run the secret deny-list scan on all rendered output, and delegate atomic
 * writes (+ CDK-022 receipts) to applyPatchPlan (tc-transform.mjs).
 *
 * ADR-0089 §scaffold-from-pattern invariants (ALL enforced):
 *   - Validators throw BEFORE any I/O.
 *   - Secret deny-list scanned on every rendered file BEFORE any write.
 *     A detected secret is REFUSED; in write mode ScaffoldSecretError is thrown
 *     (no partial scaffold ever written).
 *   - Dry-run by default; explicit write:true required to mutate.
 *   - Atomic write + one CDK-022 receipt per file via applyPatchPlan.
 *   - Scope-fenced via applyPatchPlan (fence lives in tc-transform.mjs).
 *   - Pattern registry is ADR-gated reviewable data; ships ONE inert seed.
 *
 * // consumes: economy/tc-transform
 * Zero runtime dependencies — node:* only. [task-compiler] [WF0022] [ADR-0089]
 */
import { applyPatchPlan, TransformValidationError, TransformScopeError } from './tc-transform.mjs';
export { TransformValidationError, TransformScopeError };

// ---------------------------------------------------------------------------
// Schema version + secret deny-list (mirrors tc-cache.mjs — ADR-0089)
// ---------------------------------------------------------------------------

export const TC_SCAFFOLD_SCHEMA_VERSION = 'cdk-tc-scaffold/1';

const SECRET_PATTERNS = [
  /(?:api[_-]?key|secret|token|password|passwd|auth)[^\n]{0,20}[=:]\s*['"]?\S{12,}/i,
  /AKIA[0-9A-Z]{16}/,
  /-----BEGIN (?:RSA |EC |OPENSSH |DSA )?PRIVATE KEY-----/,
  /(?:ghp|ghs|ghr|glpat|npm_)[A-Za-z0-9_]{20,}/,
  /\b[0-9a-f]{40,}\b/,
];

/**
 * Returns true when rendered content matches a known secret/credential pattern.
 * @param {string} rendered
 * @returns {boolean}
 */
function containsSecret(rendered) {
  return SECRET_PATTERNS.some((re) => re.test(rendered));
}

// ---------------------------------------------------------------------------
// Typed errors
// ---------------------------------------------------------------------------

/** Thrown when a scaffold request is malformed (before any I/O). */
export class ScaffoldValidationError extends Error {
  constructor(m) { super(`ScaffoldValidationError: ${m}`); this.name = 'ScaffoldValidationError'; }
}
/** Thrown when rendered content contains a probable secret/credential. */
export class ScaffoldSecretError extends Error {
  constructor(m) { super(`ScaffoldSecretError: ${m}`); this.name = 'ScaffoldSecretError'; }
}
/** Thrown when the named pattern is absent from the registry. */
export class ScaffoldPatternNotFoundError extends Error {
  constructor(id) {
    super(`ScaffoldPatternNotFoundError: pattern "${id}" not found in registry`);
    this.name = 'ScaffoldPatternNotFoundError';
  }
}

// ---------------------------------------------------------------------------
// Pattern registry — ADR-gated reviewable data (constitution §9)
//
// Ships exactly ONE inert seed pattern (SEED-ONLY). Any new pattern or
// modification to its transform rules requires an ADR (ADR-0089).
// ---------------------------------------------------------------------------

const SEED_TEMPLATE =
`/**
 * {{DESCRIPTION}}
 *
 * SCAFFOLD SKELETON — generated by tc-scaffold seed-inert-module v1.0.0 (ADR-0089).
 *
 * TODO(security): review before connecting to any live system.
 * Any credentials needed here must be supplied from the environment at runtime,
 * never hard-coded. See the project security guide.
 */

/**
 * {{MODULE_NAME}} — generated skeleton. Replace this stub with real logic.
 * @returns {void}
 */
export function run() {
  // TODO: implement {{MODULE_NAME}}
}`;


const PATTERN_REGISTRY = Object.freeze([
  Object.freeze({
    id:           'seed-inert-module',
    version:      '1.0.0',
    description:  'SEED-ONLY — inert module skeleton with TODO(security) markers. ADR-gated.',
    allowedPaths: ['contextkit/pipeline/scratch/'],
    variables:    ['MODULE_NAME', 'DESCRIPTION'],
    files: Object.freeze([
      Object.freeze({
        relativePath: 'contextkit/pipeline/scratch/{{MODULE_NAME}}.mjs',
        template:     SEED_TEMPLATE,
      }),
    ]),
  }),
]);

/** Number of patterns in the registry (asserted by selfcheck). */
export const PATTERN_COUNT = PATTERN_REGISTRY.length;

// ---------------------------------------------------------------------------
// Pattern lookup
// ---------------------------------------------------------------------------

/**
 * Finds a pattern by identifier. Throws before any I/O when absent.
 * @param {string} patternId
 * @returns {object}
 * @throws {ScaffoldPatternNotFoundError}
 */
export function findPattern(patternId) {
  const found = PATTERN_REGISTRY.find((p) => p.id === patternId);
  if (!found) throw new ScaffoldPatternNotFoundError(patternId);
  return found;
}

// ---------------------------------------------------------------------------
// Input validator (throws before any I/O)
// ---------------------------------------------------------------------------

/**
 * Validates a scaffold request. All checks run before pattern lookup or I/O.
 * @param {unknown} req
 * @throws {ScaffoldValidationError}
 */
function validateRequest(req) {
  if (!req || typeof req !== 'object' || Array.isArray(req)) {
    throw new ScaffoldValidationError('scaffold request must be a non-null object');
  }
  if (typeof req.patternId !== 'string' || !req.patternId.trim()) {
    throw new ScaffoldValidationError('request.patternId must be a non-empty string');
  }
  if (!req.vars || typeof req.vars !== 'object' || Array.isArray(req.vars)) {
    throw new ScaffoldValidationError('request.vars must be a non-null, non-array object');
  }
  for (const [key, val] of Object.entries(req.vars)) {
    if (typeof val !== 'string') {
      throw new ScaffoldValidationError(
        `request.vars["${key}"] must be a string, got ${typeof val}`
      );
    }
  }
}

// ---------------------------------------------------------------------------
// Template renderer
// ---------------------------------------------------------------------------

/**
 * Substitutes {{VAR}} tokens from the provided vars map.
 * Unresolved tokens are left as-is.
 * @param {string} template
 * @param {Record<string,string>} vars
 * @returns {string}
 */
function renderTemplate(template, vars) {
  return template.replace(/\{\{([A-Z_][A-Z0-9_]*)\}\}/g, (_m, name) =>
    Object.prototype.hasOwnProperty.call(vars, name) ? vars[name] : `{{${name}}}`
  );
}

// ---------------------------------------------------------------------------
// runScaffold — main export
// ---------------------------------------------------------------------------

/**
 * Materialises a scaffold pattern into new files.
 *
 * Validators throw BEFORE any I/O. The secret deny-list is scanned on every
 * rendered file before any write; a detected secret is REFUSED. In write mode
 * a detected secret throws ScaffoldSecretError (no partial scaffold). Dry-run
 * is the default; write:true triggers atomic file writes via applyPatchPlan,
 * producing one CDK-022 receipt per file.
 *
 * @param {{ patternId:string, vars:Record<string,string>, root?:string, write?:boolean }} req
 * @returns {{ dryRun:boolean, receipts:object[], preview:string[],
 *             advisoryLines:string[], refused:string[] }}
 * @throws {ScaffoldValidationError}       on malformed request (before any I/O)
 * @throws {ScaffoldPatternNotFoundError}  when patternId is not registered
 * @throws {ScaffoldSecretError}           when rendered content contains a secret
 * @throws {TransformValidationError}      forwarded from applyPatchPlan
 * @throws {TransformScopeError}           forwarded from applyPatchPlan
 */
export function runScaffold(req) {
  // Step 1: Validate BEFORE any I/O (ADR-0089 invariant).
  validateRequest(req);

  const { patternId, vars, write = false } = req;
  const root = (typeof req.root === 'string' && req.root) ? req.root : process.cwd();

  // Step 2: Pattern lookup — throws if absent (before any I/O).
  const pattern = findPattern(patternId);

  // Step 3: Render all templates and run the secret deny-list scan (before I/O).
  /** @type {Array<{path:string, rendered:string}>} */
  const renderedFiles = [];
  const refused = [];

  for (const file of pattern.files) {
    const targetPath = renderTemplate(file.relativePath, vars);
    const content    = renderTemplate(file.template, vars);

    if (containsSecret(content)) {
      refused.push(targetPath);
      if (write) {
        throw new ScaffoldSecretError(
          `Rendered content for "${targetPath}" contains a probable secret/credential. ` +
          'Scaffold refused (ADR-0089 §scaffold-from-pattern).'
        );
      }
      continue; // dry-run: record refusal, keep generating preview
    }

    renderedFiles.push({ path: targetPath, rendered: content });
  }

  // All files refused — nothing to apply.
  if (renderedFiles.length === 0) {
    return { dryRun: !write, receipts: [], preview: [], advisoryLines: [], refused };
  }

  // Step 4: Delegate to applyPatchPlan — atomic write + receipt per file.
  const patchPlan = {
    recipeId:     pattern.id,
    version:      pattern.version,
    allowedPaths: pattern.allowedPaths,
    patches:      renderedFiles.map(({ path, rendered: newContent }) => ({ path, newContent })),
  };

  const applyResult = applyPatchPlan(patchPlan, { write, root });

  // Step 5: Wrap transform receipts with the scaffold schema version.
  const receipts = (applyResult.receipts ?? []).map((r) => ({
    schemaVersion:  TC_SCAFFOLD_SCHEMA_VERSION,
    patternId:      pattern.id,
    patternVersion: pattern.version,
    path:           r.path,
    afterSha256:    r.afterSha256,
    scope:          r.scope,
    timestamp:      r.timestamp,
  }));

  return {
    dryRun:        applyResult.dryRun,
    receipts,
    preview:       applyResult.preview ?? [],
    advisoryLines: applyResult.advisoryLines ?? [],
    refused,
  };
}

// ---------------------------------------------------------------------------
// presentScaffold — human-readable summary (safe on null input)
// ---------------------------------------------------------------------------

/**
 * Renders a ScaffoldResult as a human-readable multi-line string.
 * @param {object|null|undefined} result
 * @returns {string}
 */
export function presentScaffold(result) {
  if (!result || typeof result !== 'object') {
    return `tc-scaffold [${TC_SCAFFOLD_SCHEMA_VERSION}]: invalid result`;
  }

  const mode  = result.dryRun ? 'DRY-RUN' : 'APPLIED';
  const lines = [`tc-scaffold [${TC_SCAFFOLD_SCHEMA_VERSION}]: ${mode}`];

  for (const line of result.preview ?? [])      lines.push(`  ${line}`);
  for (const line of result.advisoryLines ?? []) lines.push(`  advisory: ${line}`);
  for (const path of result.refused ?? [])       lines.push(`  REFUSED (secret): ${path}`);

  if (!result.dryRun && (result.receipts?.length ?? 0) > 0) {
    lines.push(`  receipts: ${result.receipts.length} written`);
  }

  return lines.join('\n');
}
